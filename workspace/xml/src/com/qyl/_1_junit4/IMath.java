package com.qyl._1_junit4;
/**
 * 算术运算接口
 * @author Qyuelin
 */
public interface IMath {
	/**
	 * 算术的加法运算
	 * @param num1 加数1
	 * @param num2 加数2
	 * @return 和
	 */
	int add(int num1, int num2);
	
	/**
	 * 算术的乘法运算
	 * @param num1 乘数1
	 * @param num2 乘数2
	 * @return  积
	 */
	int ride(int num1, int num2);
}
