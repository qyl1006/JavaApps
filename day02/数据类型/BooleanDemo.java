//boolean数据类型
public class BooleanDemo
{
	public static void main(String[] args)
	{
		//boolean类型的常量:true/false
		System.out.println(true);
		System.out.println(false);

		//boolean类型的变量
		//数据结构:  变量名 = 该类型的常量值
		boolean isman = true;
		System.out.println(isman);

		//给isman重新赋值false
		isman = false;
		System.out.println(isman);

	}
}