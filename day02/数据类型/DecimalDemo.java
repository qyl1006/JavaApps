//小数类型 float double类型
public class DecimalDemo
{
	public static void main(String[] args)
	{
		//小数常量(默认是double类型)
		System.out.println(3.14);  //double类型的常量
		System.out.println(3.14F); //float类型的常量
		//小数的变量
		//小数类型  变量名 = 小数常量
		double pi = 3.14;
		System.out.println(pi);

		float pi2 = 3.14F;
		System.out.println(pi2);

	}
}