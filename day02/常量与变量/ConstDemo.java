public class ConstDemo
{
	//程序的入口
	public static void main(String[] args)
	{
		/*
			字面值常量:比如:整数1, 2, 3. 小数常量3.14  布尔常量false,true等等.
			字面量就表示直接给出的一个值(可以是整数 小数 true false等等)直接量
		*/
		//打印出:常量123
		System.out.println(123);
		//打印出:常量3.14
		System.out.println(3.14);
		System.out.println(true);  //对
		System.out.println(false); //错
	}
}